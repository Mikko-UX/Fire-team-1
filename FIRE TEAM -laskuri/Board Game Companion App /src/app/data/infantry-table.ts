const DECODE_MAP: Record<string, string> = {
  E: "ALL ELIMINATED",
  F: "FLIP + OFFICER CHECK",
  "2": "ADD 2 FEAR MARKERS",
  "1": "ADD 1 FEAR MARKER",
  "0": "NO EFFECT",
};

function getColumnIndexFromFirepower(firepower: number): number {
  if (firepower > 120) return 0;
  else if (firepower >= 100) return 1;
  else if (firepower >= 80) return 2;
  else if (firepower >= 60) return 3;
  else if (firepower >= 48) return 4;
  else if (firepower >= 36) return 5;
  else if (firepower >= 32) return 6;
  else if (firepower >= 28) return 7;
  else if (firepower >= 24) return 8;
  else if (firepower >= 21) return 9;
  else if (firepower >= 18) return 10;
  else if (firepower >= 16) return 11;
  else if (firepower >= 14) return 12;
  else if (firepower >= 12) return 13;
  else if (firepower >= 10) return 14;
  else if (firepower >= 8) return 15;
  else if (firepower >= 6) return 16;
  else if (firepower >= 4) return 17;
  else return 18;
}

export interface InfantryFireResult {
  result: string;
  chosen_row_key: number;
  firepower_input: number;
  die_roll: number;
  modifiers: number;
  modified_roll: number;
  col_index: number;
  column_letter: string;
  decoded: string;
  table_coord: string;
}

export function infantryFireTableLookup(
  firepower_input: number,
  die_roll: number,
  modifiers: number = 0
): InfantryFireResult {
  const col_index = getColumnIndexFromFirepower(firepower_input);
  const column_letter = COLUMN_LETTERS[col_index];

  let modified_roll = die_roll + modifiers;

  if (modified_roll < 1) modified_roll = 1;
  else if (modified_roll > 14) modified_roll = 14;

  const raw_value = INFANTRY_FIRE_TABLE[modified_roll][col_index];
  const decoded = DECODE_MAP[raw_value] || raw_value;

  return {
    result: raw_value,
    chosen_row_key: modified_roll,
    firepower_input,
    die_roll,
    modifiers,
    modified_roll,
    col_index,
    column_letter,
    decoded,
    table_coord: `${column_letter}${modified_roll}`,
  };
}
